    public function rprint($vA_ID)
	{
		$DataSource = new ReportDataSource;
		require_once('./protected/class/class.ezpdf.php');
		$pdf = new Cezpdf();
		$pdf->selectFont('./protected/class/fonts/Courier.afm');
		
		//--Get data array 
		$mystr=$DataSource->ArrayVehicleAllocation($vA_ID);		
		$ArrCount = count($mystr);	
		
		$cri = new CDbCriteria();
        $cri->select ="Allocation_Type";
        $cri->condition="Allocation_Type_ID=$vA_ID";
        $arr = MaAllocationType::model()->findAll($cri);
        $allocationType='';
        if(count($arr)>0)
        {
            $allocationType = $arr[0]['Allocation_Type'];
        }
			//--add Heddings
		 //$pdf->ezText('Ministry of Public Administration and Home Affairs',19);
		 $pdf->ezText(Yii::app()->params['companyName'],14);
	
		 
		//$pdf->ezText('Local Governance Program',15);
		 $pdf->ezText('',2);
		 //$pdf->ezText('Fleet Management System',14);		
		 $pdf->ezText(Yii::app()->params['sysName'],12);
		 $pdf->ezText('',5);
		 $pdf->ezText('Vehicle Allocation Report',12);
		 $pdf->ezText('Type - '.$allocationType,10);
		 $pdf->ezText('',28);		
		 $pdf->addJpegFromFile("./images/NationalSymbol.jpg",'500','740','71','87');
		 //$pdf->addText(410,730,8,"<i>'Towards Next Generation Government'</i>",0);		
		
		 $pdf->ezText('',10);
		 //--add Line to report
		 $pdf->line(580,723,20,723);
		 $pdf->line(580,724.5,20,724.5);
		 //--set the table line style
		 //$pdf->setLineStyle(1);			 
		 //$pdf->line(580,20,15,20);		 
		 		 
		 $Top=690;
		 $Right=330;
		 $LeftFront=32;
		 $LeftBack=150;
		 
		
