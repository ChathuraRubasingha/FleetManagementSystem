		{
			$pdf->ezTable($mystr,array('Vehicle_No'=>'Vehicle No.','Category_Name'=>'Category','Model'=>'Model', 'Make'=>'Make','Vehicle_Status'=>'Vehicle Status','Number_of_Passenger'=>'Passengers','Fitness_test'=>'Eligible for Fitness Test'),'',
